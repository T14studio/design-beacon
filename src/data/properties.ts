export function formatPrice(price: number, mode: "Venda" | "Locação"): string {
  const formatted = new Intl.NumberFormat("pt-BR", {
    style: "currency",
    currency: "BRL",
    minimumFractionDigits: 0,
    maximumFractionDigits: 0,
  }).format(price);
  return mode === "Locação" ? `${formatted}/mês` : formatted;
}
